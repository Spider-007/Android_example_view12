package com.htmitech.emportal.ui.chart.post.entity;

import com.htmitech.emportal.entity.ReportResult;

public class EventDataPostEntity {
	String DataItemFieldValue;
	int DataItemIndex;
	String DataItemName;
	String ValueItemFieldValue;
	int ValueItemIndex;
	String Value;
	String reportGuid;
	ReportResult[] ReportParameters;
	
	public String getDataItemFieldValue() {
		return DataItemFieldValue;
	}
	public void setDataItemFieldValue(String dataItemFieldValue) {
		DataItemFieldValue = dataItemFieldValue;
	}
	public int getDataItemIndex() {
		return DataItemIndex;
	}
	public void setDataItemIndex(int dataItemIndex) {
		DataItemIndex = dataItemIndex;
	}
	public String getDataItemName() {
		return DataItemName;
	}
	public void setDataItemName(String dataItemName) {
		DataItemName = dataItemName;
	}
	public String getValueItemFieldValue() {
		return ValueItemFieldValue;
	}
	public void setValueItemFieldValue(String valueItemFieldValue) {
		ValueItemFieldValue = valueItemFieldValue;
	}
	public int getValueItemIndex() {
		return ValueItemIndex;
	}
	public void setValueItemIndex(int valueItemIndex) {
		ValueItemIndex = valueItemIndex;
	}
	public String getValue() {
		return Value;
	}
	public void setValue(String value) {
		Value = value;
	}
	public String getReportGuid() {
		return reportGuid;
	}
	public void setReportGuid(String reportGuid) {
		this.reportGuid = reportGuid;
	}
	public ReportResult[] getReportParameters() {
		return ReportParameters;
	}
	public void setReportParameters(ReportResult[] reportParameters) {
		ReportParameters = reportParameters;
	}
}
