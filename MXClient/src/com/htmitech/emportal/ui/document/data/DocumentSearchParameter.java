package com.htmitech.emportal.ui.document.data;


public class DocumentSearchParameter {

	public String pageSize;
	public String pageNum;
	public String nodeId;
	public String filename;
	public String remark;
	public String groupCorpId;

	public String getPageSize() {
		return pageSize;
	}

	public void setPageSize(String pageSize) {
		this.pageSize = pageSize;
	}

	public String getPageNum() {
		return pageNum;
	}

	public void setPageNum(String pageNum) {
		this.pageNum = pageNum;
	}

	public String getNodeId() {
		return nodeId;
	}

	public void setNodeId(String nodeId) {
		this.nodeId = nodeId;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getGroupCorpId() {
		return groupCorpId;
	}

	public void setGroupCorpId(String groupCorpId) {
		this.groupCorpId = groupCorpId;
	}
}
