
package com.htmitech.emportal.entity;

import java.io.Serializable;

public class ReportListResult implements Serializable {
	String Report_ID;
	String ReportName;
	int ReportType;
	String CreatedTime;
	String ModifiedTime;
	String DeletedFlag;						//boolean 类型
	String PublishedFlag;					//boolean 类型
	String NavigateMenuName;  //导航时使用的导航菜单名称
	
	public String getNavigateMenuName() {
		return NavigateMenuName;
	}
	public void setNavigateMenuName(String navigateMenuName) {
		NavigateMenuName = navigateMenuName;
	}
	public String getReport_ID() {
		return Report_ID;
	}
	public void setReport_ID(String report_ID) {
		Report_ID = report_ID;
	}
	public String getReportName() {
		return ReportName;
	}
	public void setReportName(String reportName) {
		ReportName = reportName;
	}
	public int getReportType() {
		return ReportType;
	}
	public void setReportType(int reportType) {
		ReportType = reportType;
	}
	public String getCreatedTime() {
		return CreatedTime;
	}
	public void setCreatedTime(String createdTime) {
		CreatedTime = createdTime;
	}
	public String getModifiedTime() {
		return ModifiedTime;
	}
	public void setModifiedTime(String modifiedTime) {
		ModifiedTime = modifiedTime;
	}
	public String getDeletedFlag() {
		return DeletedFlag;
	}
	public void setDeletedFlag(String deletedFlag) {
		DeletedFlag = deletedFlag;
	}
	public String getPublishedFlag() {
		return PublishedFlag;
	}
	public void setPublishedFlag(String publishedFlag) {
		PublishedFlag = publishedFlag;
	}

}
