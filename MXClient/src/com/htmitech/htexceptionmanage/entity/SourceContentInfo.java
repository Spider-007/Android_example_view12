package com.htmitech.htexceptionmanage.entity;

import java.io.Serializable;

/**
 * 异常详情的实体
 *@author joe
 * @date 2017-9-26 09:28:23
 */
public class SourceContentInfo implements Serializable {

    private String exceptionLogId;
    private String exceptionTopic;
    private int exceptionSource;
    private int exceptionType;
    private String exceptionContent;
    private String functionLogId;
    private String userId;
    private String functionName;
    private String behaviorName;
    private String exceptionMillis;
    private String createTime;


    public String getExceptionLogId() {
        return exceptionLogId;
    }

    public void setExceptionLogId(String exceptionLogId) {
        this.exceptionLogId = exceptionLogId;
    }

    public String getExceptionTopic() {
        return exceptionTopic;
    }

    public void setExceptionTopic(String exceptionTopic) {
        this.exceptionTopic = exceptionTopic;
    }

    public int getExceptionSource() {
        return exceptionSource;
    }

    public void setExceptionSource(int exceptionSource) {
        this.exceptionSource = exceptionSource;
    }

    public int getExceptionType() {
        return exceptionType;
    }

    public void setExceptionType(int exceptionType) {
        this.exceptionType = exceptionType;
    }

    public String getExceptionContent() {
        return exceptionContent;
    }

    public void setExceptionContent(String exceptionContent) {
        this.exceptionContent = exceptionContent;
    }

    public String getFunctionLogId() {
        return functionLogId;
    }

    public void setFunctionLogId(String functionLogId) {
        this.functionLogId = functionLogId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getFunctionName() {
        return functionName;
    }

    public void setFunctionName(String functionName) {
        this.functionName = functionName;
    }

    public String getBehaviorName() {
        return behaviorName;
    }

    public void setBehaviorName(String behaviorName) {
        this.behaviorName = behaviorName;
    }

    public String getExceptionMillis() {
        return exceptionMillis;
    }

    public void setExceptionMillis(String exceptionMillis) {
        this.exceptionMillis = exceptionMillis;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }
}
