package com.htmitech.htexceptionmanage.entity;

import java.io.Serializable;

/**
 * 异常详情
 * @author joe
 * @date 2017-07-05 12:04:57
 */
public class ExceptionDetailEntity implements Serializable{

    private int code;
    private String message;
    private String debugMsg;
    private ExceptionList result;
    public void setCode(int code) {
        this.code = code;
    }
    public int getCode() {
        return code;
    }

    public void setMessage(String message) {
        this.message = message;
    }
    public String getMessage() {
        return message;
    }

    public void setDebugMsg(String debugMsg) {
        this.debugMsg = debugMsg;
    }
    public String getDebugMsg() {
        return debugMsg;
    }

    public ExceptionList getResult() {
        return result;
    }

    public void setResult(ExceptionList result) {
        this.result = result;
    }
}
