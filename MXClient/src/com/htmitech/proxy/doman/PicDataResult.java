package com.htmitech.proxy.doman;

import mobilereport.com.chatkit.domain.Title;

/**
 * Created by htrf-pc on 2017/6/13.
 */
public class PicDataResult {
    public Titles title;
    public CenterTitle center_title;
    public PicDataMessage pieData;

    public Titles getTitle() {
        if(title == null){
            title = new Titles();
        }
        return title;
    }

    public void setTitle(Titles title) {
        this.title = title;
    }

    public CenterTitle getCenter_title() {
        if(center_title == null){
            center_title = new CenterTitle();
        }
        return center_title;
    }

    public void setCenter_title(CenterTitle center_title) {
        this.center_title = center_title;
    }

    public PicDataMessage getPieData() {
        return pieData;
    }

    public void setPieData(PicDataMessage pieData) {
        this.pieData = pieData;
    }
}
