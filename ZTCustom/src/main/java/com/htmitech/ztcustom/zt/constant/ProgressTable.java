package com.htmitech.ztcustom.zt.constant;

public class ProgressTable {
	private int jh;
	private int ap;
	private int wc;
	private double apl;
	private double wcl;

	public void setJh(int jh) {
		this.jh = jh;
	}

	public int getJh() {
		return this.jh;
	}

	public void setAp(int ap) {
		this.ap = ap;
	}

	public int getAp() {
		return this.ap;
	}

	public void setWc(int wc) {
		this.wc = wc;
	}

	public int getWc() {
		return this.wc;
	}

	public void setApl(double apl) {
		this.apl = apl;
	}

	public double getApl() {
		return this.apl;
	}

	public void setWcl(double wcl) {
		this.wcl = wcl;
	}

	public double getWcl() {
		return this.wcl;
	}

}
